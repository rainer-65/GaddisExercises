# Programming Exercise 2-5

# Variables to hold the distances.
distance6Hours = 0.0
distance10Hours = 0.0
distance15Hours = 0.0

# Constant for the speed.
SPEED = 70

# Calculate the distance the car will travel in
# 6, 10, and 15 hours.
distance6Hours = SPEED * 6
distance10Hours = SPEED * 10
distance15Hours = SPEED * 15 

# Print the results.
print ('The car will travel the following distances:')
print (f'{distance6Hours} miles in 6 hours.')
print (f'{distance10Hours} miles in 10 hours.')
print (f'{distance15Hours} miles in 15 hours.')
